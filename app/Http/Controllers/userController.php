public function search($val)
    {
      
        $myUser=User::where('firstname','LIKE',"%$val%")->orWhere('lastname','LIKE',"%$val%")->orWhere('username','LIKE',"%$val%")->orWhere('email','LIKE',"%$val%")->latest()->get();
